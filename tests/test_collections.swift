// 测试数组功能
var numbers: [Int] = [1, 2, 3, 4, 5];
print(numbers);

// 测试字典功能
var person: [String: String] = ["name": "Alice", "age": "25"];
print(person);

// 测试索引访问
print(numbers[0]);
print(person["name"]);

// 测试嵌套数组
var matrix: [[Int]] = [[1, 2], [3, 4]];
print(matrix);
print(matrix[0]);
print(matrix[0][1]);